export function useLicenseeStats() {
  return useQuery({
    queryKey: ["licensee-stats"],
    queryFn: async () => {
      const { data, error } = await supabase
        .from("licensees")
        .select("status, total_machines, total_revenue, coverage_states, coverage_cities, performance_score, address_state, address_city");

      if (error) throw error;

      // Calculate unique states and cities across all licensees
      const allStates = new Set<string>();
      const allCities = new Set<string>();
      const activeCapitals = new Set<string>();
      let totalPerformance = 0;
      let performanceCount = 0;

      data.forEach((l) => {
        // Add coverage states and address state
        (l.coverage_states || []).forEach((s: string) => allStates.add(s));
        if (l.address_state) allStates.add(l.address_state);
        
        // Add coverage cities and address city
        (l.coverage_cities || []).forEach((c: string) => allCities.add(c));
        if (l.address_city) allCities.add(l.address_city);
        
        // Check if any city is a state capital
        const citiesToCheck = [...(l.coverage_cities || []), l.address_city].filter(Boolean);
        citiesToCheck.forEach((city: string) => {
          Object.entries(STATE_CAPITALS).forEach(([state, capital]) => {
            if (city?.toLowerCase().includes(capital.toLowerCase()) || capital.toLowerCase().includes(city?.toLowerCase())) {
              activeCapitals.add(capital);
            }
          });
        });
        
        if (l.performance_score) {
          totalPerformance += Number(l.performance_score);
          performanceCount++;
        }
      });

      const stats = {
        total: data.length,
        active: data.filter((l) => l.status === "active").length,
        pending: data.filter((l) => l.status === "pending").length,
        inactive: data.filter((l) => l.status === "inactive").length,
        totalMachines: data.reduce((sum, l) => sum + (l.total_machines || 0), 0),
        totalRevenue: data.reduce((sum, l) => sum + Number(l.total_revenue || 0), 0),
        totalStates: allStates.size,
        totalCities: allCities.size,
        totalCapitals: activeCapitals.size,
        activeStates: Array.from(allStates),
        activeCapitals: Array.from(activeCapitals),
        avgPerformance: performanceCount > 0 ? totalPerformance / performanceCount : 0,
      };

      return stats;
    },
  });
}
